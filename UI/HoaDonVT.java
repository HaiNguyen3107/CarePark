    public HoaDonVT(VeThangDTO ticket) {
        initComponents();
        this.currentTicket = ticket;
        maveTxt.setText(ticket.getMaVe());
        tenkhTxt.setText(ticket.getTenKhach());
        biensoxeTxt.setText(ticket.getBienSo());
        LocalDate date = ticket.getNgayDk();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String dateString = date.format(formatter);
        ngaydangkyTxt.setText(dateString);
        int price = ticket.getLoaiXe().equalsIgnoreCase("Xe máy") ? 70000 : 1000000;
        thanhtienTxt.setText(String.valueOf(price) + " VND");

    }

