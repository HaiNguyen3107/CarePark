    private void showData() {
        dataList = VeThangDAO.findAll();

        tableModel.setRowCount(0);

        for (VeThangDTO ticket : dataList) {
            tableModel.addRow(new Object[]{
                ticket.getMaVe(),
                ticket.getTenKhach(),
                ticket.getSdt(),
                ticket.getBienSo(),
                ticket.getLoaiXe(),
                ticket.getMauXe(),
                ticket.getNgayDk(),
                ticket.getSoTien()
            });
        }
    }

    

