    private void xoaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaBtnActionPerformed
        int selectedIndex = veTable.getSelectedRow();
        if (selectedIndex >= 0) {
            VeThangDTO ticket = dataList.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this item?");

            if (option == 0) {
                VeThangDAO.delete(ticket.getMaVe());
                showData();
                lammoiBtnActionPerformed(evt);
                xoaBtn.setEnabled(false);
                suaBtn.setEnabled(false);
            }
        }
    }//GEN-LAST:event_xoaBtnActionPerformed

    private void themBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themBtnActionPerformed
        String ticketId = maVeTxt.getText();
        String customerName = tenKHTxt.getText();
        String phoneNumber = sdtTxt.getText();
        String licensePlate = biensoXeTxt.getText();
        String vehicleType = loaiXeTxt.getText();
        String vehicleColor = mauXeTxt.getText();
        LocalDate currentDate = LocalDate.now();
        int tien;
        VeThangDTO ticket = new VeThangDTO();
        if (vehicleType.equals("Xe Máy")) {
            tien = 70000;
            ticket = new VeThangDTO(ticketId, customerName, phoneNumber, licensePlate, vehicleType, vehicleColor, currentDate, 70000);
        } else {
            tien = 100000;
            ticket = new VeThangDTO(ticketId, customerName, phoneNumber, licensePlate, vehicleType, vehicleColor, currentDate, 200000);
        }
        TongVeThangDTO tk = new TongVeThangDTO(ticketId, customerName, phoneNumber, licensePlate, vehicleType, vehicleColor, currentDate, tien);
        TongVeThangDAO.insert(tk);
        HoaDonVT billsFrame = new HoaDonVT(ticket);
        billsFrame.setBillActionListener(new BillActionListener() {
            @Override
            public void onConfirm(VeThangDTO ticket) {
                VeThangDAO.insert(ticket); 
                showData(); 
                lammoiBtnActionPerformed(evt); 
            }

            @Override
            public void onCancel() {
            }
        });
        billsFrame.setVisible(true);
    }//GEN-LAST:event_themBtnActionPerformed

