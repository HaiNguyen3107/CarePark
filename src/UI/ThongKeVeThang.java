    private void traCuuBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_traCuuBtn1ActionPerformed
        if (tuDate.getDate() == null && denDate.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Chưa nhập thời gian!");
        } else if (tuDate.getDate() == null && denDate.getDate() != null) {
            JOptionPane.showMessageDialog(this, "Chưa nhập thời gian bắt đầu!");
        } else if (tuDate.getDate() != null && denDate.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Chưa nhập thời gian kết thúc!");
        } else {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String TuNgay = sdf.format(tuDate.getDate());
            String DenNgay = sdf.format(denDate.getDate());
            find(TuNgay, DenNgay);
            loadCountTime();
            loadTienTime();
        }
    }//GEN-LAST:event_traCuuBtn1ActionPerformed

