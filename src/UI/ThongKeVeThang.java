    private void loadAll() {  
        ArrayList<TongVeThangDTO> list = TongVeThangDAO.loadTableVethang();
        DefaultTableModel tblModel = (DefaultTableModel) thongKeTable.getModel();
        Object[] row = new Object[8];
        for (int i = tblModel.getRowCount() - 1; i >= 0; i--) {
            tblModel.removeRow(i);
        }
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getMaVeThang();
            row[1] = list.get(i).getTenKhach();
            row[2] = list.get(i).getSdt();
            row[3] = list.get(i).getBienSo();
            row[4] = list.get(i).getLoaiXe();
            row[5] = list.get(i).getMauXe();
            row[6] = list.get(i).getNgayDk();
            row[7] = list.get(i).getSoTien();
            tblModel.addRow(row);
        }
    }

